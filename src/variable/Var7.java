package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) true, false 입력가능
        char d = 'A'; // 문자 하나, 작은 따옴표 사용
        String e = "Hello Java"; // 문자열을 다루기 위한 특별한 타입, 큰 따옴표 사용

        System.out.println(a); // 복붙 - mac(command+d)
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
